#include <iostream>
using namespace std;

int main() {
	/// int/int --> int
	cout << 5/7 << endl;
	
	/// float/int --> float
	cout << 5.0/7 << endl;
	
	/// float/int --> float
	///cout << (float)5/7 << endl; /// CASTEAR
	
	/// int/float --> float
	cout << 5/7.0 << endl;
	
	/// float/float --> float
	cout << 5.0/7.0 << endl;
	
	
	return 0;
}

